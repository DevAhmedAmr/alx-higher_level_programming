#!/usr/bin/python3
"""Square class"""


class Square:
    """Square class"""

    def __init__(self, x):
        self.__size = x

    def area(self):
        return self.__size * self.__size

    @property
    def size(self):
        return self.__size

    @size.setter
    def size(self, size):
        if type(size) != int:
            raise TypeError("size must be an integer")
        if size < 0:
            raise ValueError("size must be >= 0")
        self.__size = size
